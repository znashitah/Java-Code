package practice;

public class calc3 { 
	static int plus(int a,int b){ 
		int pl;
		pl=a+b; 
		return pl;
		
	} 
	static int minus(int a,int b) {
		int mi; 
		mi=a-b; 
		return mi;
	}
	static int mult(int a,int b) {
		int ml; 
		ml=a*b; 
		return ml;
	} 
	static int div(int a,int b) {
		int d; 
		d=a/b; 
		return d;
	}

}
